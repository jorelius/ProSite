/** Prepend the Astro base path to an internal path */
export function url(path: string): string {
  const base = import.meta.env.BASE_URL.replace(/\/$/, "");
  return `${base}${path.startsWith("/") ? path : `/${path}`}`;
}

export function getContactHref(name: string, contact: string): string {
  switch (name) {
    case "twitter":
      return `https://www.twitter.com/${contact}`;
    case "linkedin":
      return `https://www.linkedin.com/in/${contact}`;
    case "github":
      return `https://github.com/${contact}`;
    case "email":
      return `mailto:${contact}`;
    case "rss":
      return url(contact);
    default:
      return contact;
  }
}
